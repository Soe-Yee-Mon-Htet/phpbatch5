<?php
    // Assignment Operators
    //  = , += , -= , *= , /= , %= , ++ , -- 

    // Assign ( = )
    $city = ""; // Declare // empty
    $city = null; // Declare // empty
    $city = "Yangon"; // Assign or Initialize
    $city = "Mandalay"; // Reassign or Overwrite or Override
    echo $city; // empty

    $x = 100;
    $y = 200;
    echo $x; // 100
    echo $y; // 200

    $x = $y; //200
    $x = $x + $y; //300

    $i = 10;
    echo $i; //10

    $i = $i+1; //11 or $i += 1;
    $i++; //12


    $y = 20;
    echo $i; //20

    $y = $y-1; //21 or $y -= 1;
    $y--; //22


    $num = 100;
    $num += 40; //140
    $num -= 40; //60
    $num *= 40; //4000
    $num /= 40; //2.5
    $num %= 40; //20
    echo $num;
    



?>